package n1ejercicio2;

public class GenericMethods<N, A, E> {
	private N n;
	private A i;
	private E l;
	public GenericMethods(N n, A i, E l) {
		this.n = n;
		this.i = i;
		this.l = l;
	}
	@Override
	public String toString() {
		return "GenericMethods [n=" + n + ", a=" + i + ", e=" + l + "]";
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
